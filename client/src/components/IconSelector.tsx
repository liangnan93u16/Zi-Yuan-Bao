interface IconSelectorProps {
  open: boolean;
  onOpenChange: (open: boolean) => void;
  onSelect: (iconId: string) => void;
  currentIcon?: string;
}

export function IconSelector({ 
  open, 
  onOpenChange, 
  onSelect,
  currentIcon
}: IconSelectorProps) {
  return (
    <Dialog open={open} onOpenChange={onOpenChange}>
      <DialogContent className="sm:max-w-[600px] max-h-[90vh]">
        <DialogHeader>
          <DialogTitle>选择图标</DialogTitle>
        </DialogHeader>
        
        <div className="grid grid-cols-5 gap-3 py-4 max-h-[400px] overflow-y-auto pr-2">
          {SYSTEM_ICONS.map((icon) => (
            <div 
              key={icon.id} 
              className={`
                flex flex-col items-center justify-center p-2 rounded-lg cursor-pointer transition-all
                ${currentIcon === icon.id ? 'bg-primary/10 ring-2 ring-primary' : 'hover:bg-neutral-100'}
              `}
              onClick={() => onSelect(icon.id)}
            >
              <div 
                className="h-12 w-12 mb-1 flex items-center justify-center border rounded-lg p-2 relative"
                dangerouslySetInnerHTML={{ __html: icon.svg }}
              ></div>
              <span className="text-xs text-center truncate w-full">{icon.name}</span>
              {currentIcon === icon.id && (
                <CheckCircle2Icon className="h-4 w-4 text-primary absolute top-1 right-1" />
              )}
            </div>
          ))}
        </div>
        
        <DialogFooter>
          <Button onClick={() => onOpenChange(false)}>关闭</Button>
        </DialogFooter>
      </DialogContent>
    </Dialog>
  );
}

// 根据图标ID获取SVG内容
export function getIconById(id: string): string {
  const icon = SYSTEM_ICONS.find(icon => icon.id === id);
  return icon ? icon.svg : "";
}

// 图标展示组件
interface IconDisplayProps {
  iconId?: string;
  className?: string;
}

export function IconDisplay({ iconId, className = "h-5 w-5" }: IconDisplayProps) {
  if (!iconId) return null;
  
  const icon = SYSTEM_ICONS.find(icon => icon.id === iconId);
  if (!icon) return null;
  
  return (
    <div 
      className={className}
      dangerouslySetInnerHTML={{ __html: icon.svg }}
    />
  );
}